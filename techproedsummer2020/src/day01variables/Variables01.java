package day01variables;

public class Variables01 {

	public static void main(String[] args) {
		//If you use println Java will print and go to the next line
		System.out.println("Hello World");
		System.out.println("Hello Fatih");
		
		//If you use print Java will print in the same line
		System.out.print("Hello Fatih");
		System.out.println("Hello World");
		
		//Create a boolean variable and print it on the console
		boolean retired = true;
		System.out.println (retired);
		
		boolean javaHard = false;
		System.out.println(javaHard);
		
		//Create an integer variable and print it on the console
		int age = 23;
		System.out.println(age);
		
		//Create a float variable and print it on the console
		float salary = 12.6f;
		System.out.println(salary);
		
		//Create a double variable and print it on the console
		double pi = 3.14159265359;
		System.out.println(pi);
		
		//Create a char variable and print
		char initial = 'F';
		System.out.println(initial);
		
		//Create a string and print
		String name = "Fatih";
		System.out.println(name);
	}
}
