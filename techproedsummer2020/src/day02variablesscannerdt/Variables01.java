package day02variablesscannerdt;

public class Variables01 {
	
	public static void main(String[] args) {
		//How to get min and max of primitive data types
		//byte, short, int, long, float, double
		
		//Let's find the min value of byte which is -128
		byte minValueOfByte = Byte.MIN_VALUE;
		System.out.println(minValueOfByte);
		//it is -128 success
		
		//max value of byte
		byte maxValueOfByte = Byte.MAX_VALUE;
		System.out.println(maxValueOfByte);
		
		//min and max val of int
		int minValueOfInt = Integer.MIN_VALUE;
		System.out.println(minValueOfInt);
		int maxValueOfInt = Integer.MAX_VALUE;
		System.out.println(maxValueOfInt);
		
		//Homework: Find Min and Max val of short, long, float, double
		
		
		
	}
	
	

}
